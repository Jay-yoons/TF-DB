     "COMMENT" VARCHAR2 (50 CHAR) , 
     RATIO     INTEGER  NOT NULL , 
     REVIEW_ID INTEGER  NOT NULL 
    ) 
;

ALTER TABLE REVIEW 
    ADD 
    CHECK (RATIO BETWEEN 1 AND 10) 
;

