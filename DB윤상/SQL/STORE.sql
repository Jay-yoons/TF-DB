    ADD CONSTRAINT REVIEW_PK PRIMARY KEY ( REVIEW_ID ) ;

ALTER TABLE REVIEW 
    ADD CONSTRAINT REVIEW__UN UNIQUE ( USER_ID , STORE_ID ) ;

ALTER TABLE REVIEW 
    ADD CONSTRAINT REVIEW_MV_USERS_FK FOREIGN KEY 
    ( 
     USER_ID
    ) 
    REFERENCES MV_USERS 
    ( 
     USER_ID
    ) 
    ON DELETE CASCADE 
;

ALTER TABLE REVIEW 
    ADD CONSTRAINT REVIEW_STORES_FK FOREIGN KEY 
    ( 
     STORE_ID
    ) 
    REFERENCES STORES 
    ( 
     STORE_ID
    ) 
    ON DELETE CASCADE 
;

CREATE SEQUENCE REVIEW_REVIEW_ID_SEQ 
START WITH 1 
    NOCACHE 
    ORDER ;

CREATE OR REPLACE TRIGGER REVIEW_REVIEW_ID_TRG 
BEFORE INSERT ON REVIEW 
FOR EACH ROW 
WHEN (NEW.REVIEW_ID IS NULL) 
BEGIN 
    :NEW.REVIEW_ID := REVIEW_REVIEW_ID_SEQ.NEXTVAL; 
END;
